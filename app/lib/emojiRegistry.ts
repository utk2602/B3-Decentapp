// Get list of all emoji codes
export const emojiCodes = Object.keys(customEmojis);

// Get emoji by code
export function getEmoji(code: string): any | null {
    return customEmojis[code] || null;
}

// Check if text contains custom emoji
export function hasCustomEmoji(text: string): boolean {
    return emojiCodes.some(code => text.includes(code));
}

// Parse text and return array of segments (text and emoji codes)
export type TextSegment = { type: 'text'; content: string } | { type: 'emoji'; code: string };

export function parseEmojis(text: string): TextSegment[] {
    const segments: TextSegment[] = [];
    let currentText = '';
    let i = 0;

    while (i < text.length) {
        // Check if this position starts an emoji code
        let foundEmoji = false;
        for (const code of emojiCodes) {
            if (text.slice(i).startsWith(code)) {
                // Push accumulated text
                if (currentText) {
                    segments.push({ type: 'text', content: currentText });
                    currentText = '';
                }
                // Push emoji
                segments.push({ type: 'emoji', code });
                i += code.length;
                foundEmoji = true;
                break;
            }
        }
        if (!foundEmoji) {
            currentText += text[i];
            i++;
        }
    }

    // Push remaining text
    if (currentText) {
        segments.push({ type: 'text', content: currentText });
    }

    return segments;
}
